package r.base;

import r.lang.DoubleVector;
import r.lang.IntVector;
import r.lang.Null;

public class ComplexGroup {

  public static DoubleVector Mod(DoubleVector x) {
    return x;
  }
 
  public static IntVector Mod(IntVector x) {
    return x;
  }

  public static Null Mod(Null x) {
    return x;
  }
}
